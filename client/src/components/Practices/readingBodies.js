export function normalizeReadingLang(languageCode) {
  const c = String(languageCode || 'ru')
    .toLowerCase()
    .split(/[-_]/)[0];
  if (c === 'en') return 'en';
  if (c === 'kk') return 'kk';
  return 'ru';
}

export function articleBodyFor(languageCode, articleId) {
  const lang = normalizeReadingLang(languageCode);
  const row = ARTICLE_BODIES[articleId];
  if (!row) return '';
  return row[lang] || row.ru || '';
}

export function bookDescFor(languageCode, bookId) {
  const lang = normalizeReadingLang(languageCode);
  const row = BOOK_DESCS[bookId];
  if (!row) return '';
  return row[lang] || row.ru || '';
}
